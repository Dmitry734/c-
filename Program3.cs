int a, N;

Console.Write("Задача 19: \n");
Console.Write("Программа, которая принимает на вход пятизначное число и проверяет, является ли оно палиндромом. \n");
Console.Write("Введите число: ");
int.TryParse(Console.ReadLine()!, out a);

if (a / 10000 != 0 && a / 10000 < 9)
{
    if (a / 10000 == a % 10 && (a / 1000 - a / 10000 * 10) == (a % 100 - a % 10) / 10)
    {
        Console.Write($"является ли число палиндромом: ДА\n");
    }
    else
    {
        Console.Write($"является ли число палиндромом: НЕТ\n");
    }
}
else
{
    Console.Write("Введено не пятизначное число\n");
}
Console.Write("=============================\n");

