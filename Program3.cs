//Задача 21
Console.Write("Задача 21: \n");
Console.Write("Программа, которая принимает на вход координаты двух точек и находит расстояние между ними в 3D пространстве.\n");

int[] A_coord = new int[3];
int[] B_coord = new int[3];

Console.Write("Введите X координату первой точки: ");
int.TryParse(Console.ReadLine()!, out A_coord[0]);
Console.Write("Введите Y координату первой точки: ");
int.TryParse(Console.ReadLine()!, out A_coord[1]);
Console.Write("Введите Z координату первой точки: ");
int.TryParse(Console.ReadLine()!, out A_coord[2]);

Console.Write("Введите X координату второй точки: ");
int.TryParse(Console.ReadLine()!, out B_coord[0]);
Console.Write("Введите Y координату второй точки: ");
int.TryParse(Console.ReadLine()!, out B_coord[1]);
Console.Write("Введите Z координату второй точки: ");
int.TryParse(Console.ReadLine()!, out B_coord[2]);

int x = (A_coord[0] - B_coord[0]);
int y = (A_coord[1] - B_coord[1]);
int z = (A_coord[2] - B_coord[2]);


double distance = Math.Round(Math.Sqrt(x * x + y * y + z * z), 2);
Console.Write($"Расстояние между заданными (координатами) точками = {distance}\n");
Console.Write("=============================\n");

